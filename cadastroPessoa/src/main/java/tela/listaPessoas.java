    public interface ListPessoasListener {

        void listPessoasAtualizada();
    }

    public void addListPessoasListener(ListPessoasListener listener) {
        listeners.add(listener);
    }

    void notifyListeners() {
        for (ListPessoasListener listener : listeners) {
            listener.listPessoasAtualizada();
        }
    }

    public void Adcionar(classeCadastro cad) {
        pessoas.add(cad);
        ListarPessoas();

    }

    public void ListarPessoas() {
        textPessoas.setText("");
        for (int i = 0; i < pessoas.size(); i++) {
            String texto = "\nID: " + i + "- Nome: " + pessoas.get(i).getNome()
                    + "- EndereÃ§o: " + pessoas.get(i).getEndereco();
            System.out.println(texto);
            textPessoas.setText(textPessoas.getText() + texto);
        }
    }

    public void Deletar(int id) {
        pessoas.remove(id);
    }

